import turtle
class segments:
    def __init__(self, my_turtle, color, digit):
        self.my_turtle = my_turtle
        self.color = color
        self.digit = digit

    def init(self):
        turtle.speed(0)
        turtle.tracer(0)
        turtle.hideturtle()
        turtle.colormode(255)

        self.my_turtle.color(self.color)
        self.my_turtle.penup()
        self.my_turtle.setheading(0)
        self.my_turtle.goto(0, 0)
        self.my_turtle.pensize(10)


